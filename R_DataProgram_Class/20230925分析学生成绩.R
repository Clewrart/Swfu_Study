1. 读取数据,并查看数据的摘要统计信息(summary)

题目:读取成绩数据,用summary()查看数据的摘要统计信息。

解答:使用read.csv()读取数据,然后对数据使用summary()可以查看各列的基本统计量如平均值、中位数、最大最小值等,对了解数据概况非常有用。

代码:
  ```r
data <- read.csv("成绩.csv") 
summary(data)
```

2. 选出数学成绩大于90分的学生姓名(选择行)

题目:用行条件选择,选出数学成绩大于90分的学生的所有信息。

解答:可以用data[条件判断, ]的方式选择满足条件的行。这里判断数学成绩那列大于90,就可以过滤出符合要求的学生。

代码:
  ```r
data[data$math>90,]
```

3. 计算各科平均成绩(apply计算列平均值)

题目:用apply()对各科成绩分别计算平均值。

解答:apply(数据,2,mean)可以对数据按列应用mean()计算平均值。2表示按列操作。

代码:  
  ```r
apply(data[,2:6],2,mean)
```

4. 绘制各科成绩的箱线图(boxplot)

题目:用箱线图比较显示各科成绩的分布情况。

解答:boxplot(数据)可以直接画出数据的箱线图,voutlier.shape=NA可以去掉异常点。

代码:
  ```r
boxplot(data[,2:6],voutlier.shape=NA)
```

5. 计算每个学生的总分(行求和)

题目:计算每个学生4门课程的总成绩。

解答:用rowSums()可以直接对数据行求和,得到每个学生的总分。

代码:
  ```r
rowSums(data[,2:6])
```

6. 给每个学生的总分按从高到低排序(order排序)  

题目:将学生总分由高分到低分排序。

解答:sort()进行排序,decreasing=T表示由大到小排序。

代码:
  ```r  
sort(rowSums(data[,2:6]),decreasing=T)
```

7. 选出前10名成绩最好的学生(head选择前10行)

题目:选择排序后成绩最高的前10名学生。

解答:head()可以选择排序后数据的前N行。

代码:
  ```r
head(sort(rowSums(data[,2:6]),decreasing=T),10) 
```

8. 计算每个学生的平均分(apply计算行平均值)

题目:计算每个学生4门课的平均分。

解答:apply(数据,1,mean)可以对数据按行应用mean(),计算各行的平均值。

代码: 
  ```r
apply(data[,2:6],1,mean)
```

9. 绘制每个学生的平均分柱状图(barplot)

题目:用柱状图展示每个学生的平均分。

解答:barplot()可以直接绘制柱状图。加入名字可以使图更清晰。

代码:
  ```r
barplot(apply(data[,2:6],1,mean),names.arg=1:nrow(data))
```

10. 计算每个学科不及格(<60)的学生数量(apply + sum)

题目:统计每个学科成绩不及格人数。

解答:用apply()结合sum()判断成绩<60的个数即为不及格人数。

代码:
  ```r 
apply(data[,2:6],2,function(x) sum(x<60))
```

11. 找出同时不及格两门以上课程的学生(apply计算行逻辑判断)

题目:找到不及格两门及以上课程的学生。

解答:apply()对行判断成绩<60的个数,SUM>=2即为不及格两门以上。

代码:
  ```r
fail <- apply(data[,2:6],1,function(x) sum(x<60)>=2)
data[fail,]
```

12. 将学生总分划分为A-F等级(cut进行分级)

题目:根据总分将学生成绩等级划分为A-F。

解答:cut()可以将数值分组,按照分数段设置breaks进行成绩的分级。

代码:
  ```r
grades <- cut(rowSums(data[,2:6]),breaks=c(0,240,280,320,360,400)) 
```

13. 计算每个等级学生的数量(table计数)

题目:统计各个成绩等级A-F学生的人数。

解答:table()可以直接对因子类型进行计数统计。

代码:
  ```r
table(grades)
```

14. 绘制学生总分的密度分布图(density plot)  

题目:用密度曲线展示学生总分的分布情况。

解答:plot(density())可以绘制数据的密度曲线。

代码: 
  ```r 
plot(density(rowSums(data[,2:6])))
```

15. 用箱线图显示每个学科的分布(boxplot按列)

题目:通过箱线图比较各科成绩的分布情况。

解答:按列绘制箱线图,添加科目名称。

代码:
  ```r
boxplot(data[,2:6], names=colnames(data[,2:6]))
```

16. 计算每个学生的成绩标准差(apply计算行标准差)

题目:计算各个学生4门课成绩的标准差。

解答:apply(数据,1,sd)可计算各行的标准差。

代码:
  ```r 
apply(data[,2:6], 1, sd)
```

17. 绘制成绩标准差的直方图(hist)

题目:用直方图展示学生成绩标准差的分布。

解答:hist()可用来绘制直方图。

代码:
  ```r
hist(apply(data[,2:6], 1, sd)) 
```

18. 对数学成绩进行标准化(scale) 

题目:使用scale()对数学成绩进行标准化处理。

解答:scale()可以自动进行标准化,将值转换为以0为均值、1为标准差的分布。

代码:
  ```r
scale(data$math)
```